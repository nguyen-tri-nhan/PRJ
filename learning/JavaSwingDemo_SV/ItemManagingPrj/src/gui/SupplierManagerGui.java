    private void tblListSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListSupplierMouseClicked
        // TODO add your handling code here:
        int pos = tblListSupplier.getSelectedRow(); // Lấy dòng được chọn trên Table
        
        Supplier supplier = model.getData().get(pos);           
        this.txtCode.setText(supplier.getSupCode());
        this.txtName.setText(supplier.getSupName());
        this.txtAddress.setText(supplier.getAddress());
        this.rbMale.setSelected(supplier.isColloborating());
        this.rbFemal.setSelected(!supplier.isColloborating());
    }//GEN-LAST:event_tblListSupplierMouseClicked

    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
     
        Supplier supplier = new Supplier(this.txtCode.getText(), this.txtName.getText(), this.txtAddress.getText(), rbMale.isSelected());
        
        if(SupplierChecker.checkData(supplier)){
            try {
                // Thực hiện tạo nhà cung cấp mới trong CSDL
                supplierDao.create(supplier);
                
                // Cập nhật lại giao diện Table
                model.getData().add(supplier);
                tblListSupplier.updateUI();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Lỗi nhập dữ liệu");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Supplier supplier = new Supplier(this.txtCode.getText(), this.txtName.getText(), this.txtAddress.getText(), rbMale.isSelected());
        
        if(SupplierChecker.checkData(supplier)){
            try {
                // Thực hiện cập nhật CSDL nhà cung cấp
                supplierDao.update(supplier);
                
                // Cập nhật lại giao diện Table
                int pos = tblListSupplier.getSelectedRow();
                model.getData().set(pos, supplier);             
                tblListSupplier.updateUI();
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Lỗi nhập dữ liệu");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            // TODO add your handling code here:
            // Thực hiện xóa nhà cung cấp trong CSDL
            supplierDao.delete(txtCode.getText());
            
            // Cập nhật lại giao diện Table
            int pos = tblListSupplier.getSelectedRow();
            model.getData().remove(pos);
            tblListSupplier.updateUI();
            
            // Cập nhật lại giao diện text filed
            txtCode.setText("");
            txtName.setText("");
            txtAddress.setText("");
            rbMale.setSelected(true);
            
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

