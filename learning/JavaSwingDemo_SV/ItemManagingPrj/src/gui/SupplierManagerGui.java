    private void loadDataToTable(){
        try{
            Vector<Supplier> listSupplier = new Vector<Supplier>();
            // Lấy danh sách nhà cung cấp từ CSDL
            listSupplier = supplierDao.readAll();
            
            // Nạp dữ liệu từ nhà cung cấp listSupplier vào model để hiện thị trên bảng
            model.getData().addAll(listSupplier);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }
    
